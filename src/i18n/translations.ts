/**
 * Maps device languageCode (BCP-47 primary subtag) to a supported app language.
 * Falls back to 'en' if not in the supported list.
 */
export function deviceLocaleToLanguage(languageCode: string | null | undefined): SupportedLanguage {
  if (!languageCode) return 'en';
  const code = languageCode.toLowerCase().split('-')[0]; // 'zh-Hans' → 'zh'
  const supported: SupportedLanguage[] = ['en', 'fr', 'es', 'pt', 'ar', 'zh', 'ja'];
  return supported.includes(code as SupportedLanguage)
    ? (code as SupportedLanguage)
    : 'en';
}
